    public static void put(String key, VarPackage value) {
        boolean isContainsKey = dataMap.containsKey(key);
        if (isContainsKey) {
            try {
                throw new Exception("key already exists");
            } catch (Exception e) {
                e.printStackTrace();
            }
        }

        dataMap.put(key, value);

    }


    /**
     * 得到一个不重复的key
     *
     * @param key 期望key
     * @return
     */
    public static String getDataMapKey(String key) {

        int suffix = 0;
        while (true) {
            String getKey = key + suffix;
            if (!dataMap.containsKey(getKey)) {
                return getKey;
            }
            suffix++;
        }

    }

    public static Map<String, VarPackage> getMap() {
        return dataMap;
    }

